    <p>
        The Australian Property Management Alliance is founded by nine Chinese property management owners on the 17th of December 2014, including Paul Shih, Carl Wu, Alex Yang, Kevin Yuan, Phoenix Wu, Dandan Lu, Michael Chang, Gary Liang and Leo Liu. 
        The Alliance is formed to combine the resources and networks of the Chinese Property management right operators, to end the situation of every manager fighting or struggling alone within the industry by themselves. 
        The aim of the Alliance is to unite everyone, forming a strong organisation to increase the voices of Chinese operators within the competitive property management industry. 
        The Alliance will achieve economy of scale to reduce cost of service, improve quality of our management services, and provide Chinese management right owners and operators with convenience and support and be the home of all property managers. 
        The Alliance will gradually increase our variety and quality of services, and build our website to become the one stop service centre and a communication platform between proper owners and managers. 
        We welcome all onsite managers, management right owners and any interested parties to join us and cooperate to contribute towards the Australian Property Management industry. 
